export const DEFAULT_THEME = 'lavender';
export const THEME_KEYS    = Object.keys(THEMES);

const BG_PROPS = new Set([
  '--widget-bg', '--card-bg', '--titlebar-bg', '--overlay-bg', '--modal-bg',
]);

function scaleAlpha(val, mult) {
  return val.replace(/rgba?\(([^,]+),([^,]+),([^,]+),([^)]+)\)/,
    (_, r, g, b, a) => `rgba(${r},${g},${b},${Math.max(0.04, parseFloat(a) * mult).toFixed(3)})`);
}

export function applyTheme(name, bgAlpha = 1.0) {
  const theme = THEMES[name] || THEMES[DEFAULT_THEME];
  const root  = document.documentElement;
  Object.entries(theme.vars).forEach(([prop, val]) => {
    root.style.setProperty(prop, bgAlpha < 0.995 && BG_PROPS.has(prop) ? scaleAlpha(val, bgAlpha) : val);
  });
}
